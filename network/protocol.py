import struct
import json


def encode_msg(msg: dict) -> bytes:
    body = json.dumps(msg, ensure_ascii=False).encode("utf-8")
    return struct.pack("<I", len(body)) + body


def decode_msg(data: bytes) -> dict:
    return json.loads(data.decode("utf-8"))


